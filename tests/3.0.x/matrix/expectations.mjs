/**
 * Classifies file-level diffs between v4 and v5 generated output.
 *
 * Strategy: normalise both sides through a series of rewrites that map known
 * v5-only output back to its v4 equivalent (or vice versa). After
 * normalisation, byte-compare. If equal, the diff is explained by the
 * collected rules. Otherwise it's an `unexpected` regression.
 *
 * Each rule has an `id` (logged in the report so we can trace any
 * normalisation back to a documented migration-guide section).
 */

// ─── Normalisation rules ────────────────────────────────────────────────────
//
// Each rule applies to v4 and/or v5 content and returns the normalised string.
// All rules run in order on both sides.

const RULES = [
  // 1. Banner: strip the `/* Generated by Kubb */` header on both sides.
  {
    id: 'banner',
    apply: (s) => s.replace(/^\s*\/\*[\s\S]*?\*\/\s*/, ''),
  },

  // 2. Whitespace: collapse runs of blank lines so cosmetic differences don't matter.
  {
    id: 'blank-lines',
    apply: (s) => s.replace(/\n{3,}/g, '\n\n').trim() + '\n',
  },

  // 3. v5 adds `@example <value>` JSDoc lines from the OpenAPI `example` field.
  {
    id: 'jsdoc-example',
    apply: (s) => s.replace(/^\s*\*\s*@example\s+.*$\n?/gm, ''),
  },

  // 4. v5 adds `@type object` JSDoc on object schemas; v4 didn't.
  {
    id: 'jsdoc-type-object',
    apply: (s) => s.replace(/^\/\*\*\s*\n\s*\*\s*@type\s+object\s*\n\s*\*\/\s*\n/gm, ''),
  },

  // 5. v4 had `@type integer | undefined, int64` (format suffix). v5 dropped the format.
  {
    id: 'jsdoc-type-format-suffix',
    apply: (s) => s.replace(/@type\s+([a-zA-Z|\s]+),\s*(int64|int32|float|double|date-time|date|byte|binary|password|email|uri|url|uuid|ipv4|ipv6|hostname)/g, '@type $1'),
  },

  // 6. integerType default change (bigint ↔ number). Normalise both to `number`
  // when the cell didn't explicitly request bigint.
  {
    id: 'integer-bigint-to-number',
    apply: (s) => s.replace(/(:\s*)bigint(\b)/g, '$1number$2'),
  },

  // 7. v5 emits `(string & {})` open string unions; v4 emitted `string`.
  {
    id: 'open-string-union',
    apply: (s) => s.replace(/\(string\s*&\s*\{\s*\}\)/g, 'string'),
  },

  // 8. v4 sometimes emitted `enum X { ... }`; v5 emits as-const objects. We
  // can't structurally rewrite these without an AST. Both sides keep their form
  // and the diff is flagged — but tests for enumType: 'literal' should produce
  // literal unions on both sides and match identically.

  // 9. Plugin-client/react-query type renames:
  //    AddPet200      → AddPetStatus200
  //    AddPet405      → AddPetStatus405
  //    AddPetMutationRequest  → AddPetData
  //    AddPetMutationResponse → AddPetResponse
  //    AddPetMutation (aggregate) → split into AddPetRequestConfig + AddPetResponses + AddPetResponse
  //
  // Strategy: normalise v5 names back to the v4 names.
  {
    id: 'plugin-client-status-rename',
    apply: (s) => s.replace(/([A-Z][a-zA-Z0-9]+)Status(\d{3})/g, '$1$2'),
  },
  {
    id: 'plugin-client-data-rename',
    apply: (s) => s.replace(/([A-Z][a-zA-Z0-9]+)Data\b/g, '$1MutationRequest'),
  },
  {
    id: 'plugin-client-response-rename',
    apply: (s) =>
      s
        .replace(/([A-Z][a-zA-Z0-9]+)Response\b/g, '$1MutationResponse')
        .replace(/([A-Z][a-zA-Z0-9]+)Responses\b/g, '$1MutationResponse_PLURAL'),
  },

  // 10. v5 strips/changes import paths. Normalise the `from '...'` strings
  // by removing the `.ts` extension (which v5 emits and v4 doesn't, or vice versa).
  {
    id: 'import-ts-extension',
    apply: (s) => s.replace(/from\s+'(\.\.?\/[^']+?)\.ts'/g, "from '$1'"),
  },

  // 11. v4/v5 may differ in `as const` literal type — fold to a common form.
  {
    id: 'as-const-spacing',
    apply: (s) => s.replace(/\s+as\s+const/g, ' as const'),
  },

  // 12. v5 cypress method casing: 'POST' → 'post' to match v4
  {
    id: 'cypress-method-case',
    apply: (s) => s.replace(/method:\s*'(GET|POST|PUT|DELETE|PATCH)'/g, (_, m) => `method: '${m.toLowerCase()}'`),
  },

  // 13. v5 zod chained .optional() — convert v4's z.optional(...) wrapping to match
  {
    id: 'zod-chained-optional',
    apply: (s) => {
      let prev
      do {
        prev = s
        s = s.replace(/z\.optional\(\s*([^()]*(?:\([^)]*\)[^()]*)*)\s*\)/g, '$1.optional()')
      } while (s !== prev)
      return s
    },
  },

  // 14. Faker `Required<T>` return type unification
  {
    id: 'faker-required-return',
    apply: (s) => s.replace(/:\s*Required<([A-Z][\w]*)>/g, ': $1'),
  },

  // 15. Trailing semicolon / comma style — common difference between formatters.
  {
    id: 'trailing-comma-vs-semi',
    apply: (s) => s.replace(/([;,])\s*\n/g, ';\n'),
  },

  // 16. PluginMsw: HttpResponseResolver typing — v5 only. Strip from imports/types.
  {
    id: 'msw-handler-resolver',
    apply: (s) =>
      s
        .replace(/import\s+type\s*\{\s*HttpResponseResolver\s*\}\s*from\s*'msw'\s*\n?/g, '')
        .replace(/HttpResponseResolver<[^>]+>/g, 'unknown'),
  },

  // 17. PluginMcp: RequestHandlerExtra typing — v5 only.
  {
    id: 'mcp-request-handler-extra',
    apply: (s) =>
      s
        .replace(/import\s+type\s*\{[^}]*RequestHandlerExtra[^}]*\}\s*from\s*'[^']+'\s*\n?/g, '')
        .replace(/,\s*request:\s*RequestHandlerExtra<[^>]+>/g, '')
        .replace(/,\s*request\b/g, ''),
  },

  // 18. v5 always uses double-quoted URLs (template strings) but v4 sometimes uses single quotes.
  //     Normalise by removing `` ` `` template-string backticks when no interpolation.
  {
    id: 'url-quote-style',
    apply: (s) => s.replace(/`([^`${}]+)`/g, "'$1'"),
  },

  // 19. v5 emits `(string & {})` enum key types in some places; the rest of the
  //     enum block may rename. We've handled the open union above.

  // 20. Comment-only diffs (banner already stripped). Normalise leading whitespace.
  {
    id: 'leading-whitespace',
    apply: (s) => s.replace(/[ \t]+$/gm, ''),
  },
]

export function normalize(s) {
  const trace = []
  let cur = s
  for (const r of RULES) {
    const next = r.apply(cur)
    if (next !== cur) trace.push(r.id)
    cur = next
  }
  return { result: cur, rules: trace }
}

/**
 * Classify a v4 vs v5 diff on a single file. Returns:
 *   { verdict: 'identical' }             — byte-identical pre-normalisation
 *   { verdict: 'expected', rules: [...] } — equal after applying these rules
 *   { verdict: 'unexpected' }            — still differs after normalisation
 *   { verdict: 'v4-only' / 'v5-only' }   — file in only one side
 */
export const expectations = [
  {
    id: 'normalise',
    match(_filePath, v4, v5) {
      if (v4 == null || v5 == null) return false
      const n4 = normalize(v4).result
      const n5 = normalize(v5).result
      return n4 === n5
    },
  },
]
